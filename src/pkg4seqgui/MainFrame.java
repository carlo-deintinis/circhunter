            // Defining log file
            cmd[2] += " >& ./outputScript";
            
            // Debug for output string
            JOptionPane.showMessageDialog(this, "HashCirc workflow task was scheduled","Confermation",JOptionPane.INFORMATION_MESSAGE); 
            
